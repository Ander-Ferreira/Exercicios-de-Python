#Lê dois valores inteiros e compara ambos

x = int(input('Digite um valor inteiro: '))
y = int(input('Digite outro valor inteiro: '))

if(x > y):
    print(f'{x} maior do que {y}')

if(x < y):
    print(f'{x} é menor do que {y}')

