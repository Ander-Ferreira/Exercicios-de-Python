'''
not =  não, ele troca a lógica de uma variável, exemplo a = true, b =  not a -> a se torna false
and =  as duas saídas precisam ser true 
or =  só uma saída precisa ser true
'''
#Boleanos
a = False
b = not a
print(b)

c = False
d = True
e = c and d
print(e)

f = True
g = False
h = f or g
print(h)


