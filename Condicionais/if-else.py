#Lê dois valores e compara ambos

x = int(input('Digite um valor inteiro: '))
y = int(input('Digite outro valor: '))

if(x > y):
    print(f'{x} é maior do que {y}')

else:
    print(f'{x} é menor do que {y}')

#Par ou ímpar

x = int(input('Digite um valor inteiro: '))
if(x % 2 == 0):
    print(f'{x} é par')
else:
    print(f'{x} é impar')

