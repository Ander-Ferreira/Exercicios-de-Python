#Desenvolva um algoritmo que converta a temperatura em Celsius (C) para Farenheit (F)
#A equação é 9 x Celsius, tudo dividido por 5 somado a 32

celsius = float(input('Digite a temperatura: '))
celsiusFaren = (9 * celsius) / 5 + 32
print(f'São {celsius}ºC, e {celsiusFaren}ºF')

